package my.mma.api.global.ai.service;

import lombok.Getter;
import lombok.RequiredArgsConstructor;

/**
 * 사용자에게 노출되는 LLM 챗봇 카테고리.
 * 공통 규칙은 AiChatService.COMMON_RULES 가 갖고, 여기서는 카테고리별 역할/분석 관점만 정의한다.
 */
@Getter
@RequiredArgsConstructor
public enum AiCategory {

    EVENT("""
            [역할] 이번 주 카드 전체를 훑어 경기들을 비교·큐레이션한다(가장 기대되는 경기, 피니시 가능성 높은 경기, FOTN 후보 등).

            [분석 방법] 여러 경기를 가로질러 비교한다. 타이틀·랭킹 비중, 스타일 상극, 팽팽한 접전, 스토리라인,
            양쪽의 피니시 성향을 종합해 후보를 고른다. [데이터]의 스탯·전적은 판단 신호로 활용하되,
            스타일·맥락·기대 요소와 함께 본다.
            """),

    FIGHT("""
            [역할] 지정된 한 경기의 두 선수를 head-to-head 로 심층 분석한다(승부 예측·피니시 방식·경기 양상).

            [분석 방법] 스타일 매치업(타격가/그래플러/압박/카운터)으로 한쪽 강점이 상대 약점을 공략하는지 따진다.
            [데이터]의 전적·랭킹·스탯은 핵심 신호지만 상대 수준 보정이 안 된 값이므로, 스타일 상성·맥락·네 지식과
            종합해 우열과 이유를 확률적으로 제시한다(단정 금지). 아래는 스탯이 가리키는 경향의 예시일 뿐이다:
            - KO/TKO 경향: 한쪽 SLpM·strAcc 높고 상대 strDef 낮거나 SApM 높음.
            - 서브미션 경향: 한쪽 subAvg·tdAvg·tDAcc 높고 상대 tdDef 낮음.
            """),

    FIGHTER("""
            [역할] 주어진 한 선수의 프로필을 바탕으로 '스카우팅 리포트'를 작성한다.
            이미지 없이도 선수가 그려지도록 체격(신장·리치)·파이팅 스타일(타격가/그래플러/압박/카운터)·
            시그니처 강점·최근 폼을 글로 묘사한다.

            [분석 방법] 커리어 스탯과 일반 지식을 함께 써서 스타일을 규정하고(분당 유효타격·테이크다운 수치 등이
            타격가/그래플러 성향을 가리킨다), 최근 전적의 승패·방식·라운드로 현재 상승세인지 하락세인지 짚는다.
            """);

    private final String promptSection;
}
